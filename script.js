function formatReadableText(text) {
    let source = String(text || "").trim();
    if (!source) return "";

    // 脳内思考ログ：「 / 」区切りを1行ずつに展開
    if (source.includes(" / ")) {
        return source.split(" / ").map(s => s.trim()).filter(Boolean).join("\n");
    }

    // 連続スペースを正規化（改行は後で処理するため\s+ではなく半角スペースのみ対象）
    source = source.replace(/ {2,}/g, " ").trim();

    // 「 ・」を「\n・」に変換して箇条書きを行分割
    source = source.replace(/ ・/g, "\n・");

    const lines = source.split("\n").map(s => s.trim()).filter(Boolean);
    const blocks = [];
    let bulletGroup = [];

    function flushBullets() {
        if (bulletGroup.length > 0) {
            blocks.push(bulletGroup.join("\n"));
            bulletGroup = [];
        }
    }

    function processText(block) {
        const sentences = block.match(/[^。！？!?]+[。！？!?」』）)]*|[^。！？!?]+$/g) || [block];
        let buf = "", cnt = 0;

        sentences.forEach(s => {
            const chunk = s.trim();
            if (!chunk) return;

            // 「〇つ目」番号項目は単独の段落にする
            if (/^[0-9１-９]つ目/.test(chunk) || /^[一二三四五六七八九]つ目/.test(chunk)) {
                if (buf) { blocks.push(buf); buf = ""; cnt = 0; }
                blocks.push(chunk);
                return;
            }

            buf += chunk;
            cnt++;

            if (buf.length >= 80 || cnt >= 2) {
                blocks.push(buf);
                buf = ""; cnt = 0;
            }
        });

        if (buf) blocks.push(buf);
    }

    lines.forEach(line => {
        if (line.startsWith("・")) {
            // 箇条書き行：末尾に続きテキストが混在する場合（例：「・最後のitem 続きの文章」）を分離
            const contStart = line.search(/ [^・]/);
            if (contStart !== -1) {
                bulletGroup.push(line.slice(0, contStart));
                flushBullets();
                const continuation = line.slice(contStart + 1).trim();
                if (continuation) processText(continuation);
            } else {
                bulletGroup.push(line);
            }
        } else {
            flushBullets();
            processText(line);
        }
    });

    flushBullets();

    return blocks.join("\n\n");
}

function formatReadableTextForMobile(text) {
    let source = String(text || "").trim();
    if (!source) return "";

    source = source
        .replace(/\r\n?/g, "\n")
        .replace(/[ \t]+/g, " ")
        .replace(/\s*\/\s*(?=「)/g, "、\n")
        .replace(/」(?=「)/g, "」、\n")
        .replace(/」と「/g, "」と、\n「")
        .replace(/。\s*(?=「)/g, "。\n\n")
        .replace(/\s*・\s*/g, "\n・")
        .trim();

    const lines = source.split(/\n+/).map(line => line.trim()).filter(Boolean);
    const blocks = [];
    let bulletGroup = [];

    const flushBullets = () => {
        if (bulletGroup.length) {
            blocks.push(bulletGroup.join("\n"));
            bulletGroup = [];
        }
    };

    const pushParagraphs = (line) => {
        const sentences = line.match(/[^。！？!?]+[。！？!?」』）)]*|[^。！？!?]+$/g) || [line];
        let buffer = "";
        let count = 0;

        sentences.forEach(sentence => {
            const chunk = sentence.trim();
            if (!chunk) return;

            if (/^[一二三四五六七八九十0-9].{0,5}つ目/.test(chunk)) {
                if (buffer) {
                    blocks.push(buffer);
                    buffer = "";
                    count = 0;
                }
                blocks.push(chunk);
                return;
            }

            buffer += chunk;
            count += 1;

            if (buffer.length >= 74 || count >= 2) {
                blocks.push(buffer);
                buffer = "";
                count = 0;
            }
        });

        if (buffer) blocks.push(buffer);
    };

    lines.forEach(line => {
        if (line.startsWith("・")) {
            bulletGroup.push(line);
            return;
        }

        flushBullets();
        pushParagraphs(line);
    });

    flushBullets();

    return blocks.join("\n\n");
}

function normalizeDocxTextForDisplay(text) {
    return String(text || "")
        .replace(/\r\n?/g, "\n")
        .replace(/[ \t]+\n/g, "\n")
        .replace(/\n[ \t]+/g, "\n")
        .replace(/\n{3,}/g, "\n\n")
        .trim();
}

