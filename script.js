function getResultUrl(typeCode) {
    const url = new URL(SITE_URL);
    if (typeCode && db[typeCode]) {
        url.searchParams.set("type", typeCode);
    }
    return url.toString();
}

function updateUrlForType(typeCode) {
    if (!typeCode || !db[typeCode] || !window.history || !window.location) return;
    const url = new URL(window.location.href);
    url.searchParams.set("type", typeCode);
    window.history.replaceState(null, "", url);
}

function clearTypeUrl() {
    if (!window.history || !window.location) return;
    const url = new URL(window.location.href);
    url.searchParams.delete("type");
    window.history.replaceState(null, "", url);
}

